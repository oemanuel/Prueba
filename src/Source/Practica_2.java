    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
      cp=false; 
        for(i=1;i<=4;i++){
              if( pos.getText().equals(vect1[i])){ult=vect1[i];vect1[i]="O"; cp=true; }
         else if(pos.getText().equals(vect2[i])){ ult=vect2[i]; vect2[i]="O"; cp=true;}  
         else if(pos.getText().equals(vect3[i])){ ult=vect3[i]; vect3[i]="O"; cp=true;}
         else if(pos.getText().equals(vect4[i])){ ult=vect4[i]; vect4[i]="O"; cp=true;}
       }
       if(cp==false){ JOptionPane.showMessageDialog(null, "NO SE PUEDE COMPRA "+ult+ " YA ESTA OCUPADA"); }
       silla.setText("");
       pos.setText("");
      env=env+1;
      if(env!=1){comprar.setEnabled(true);}
      mos=mos+1;
      if(mos!=1){mostar.setEnabled(true);}
      enviar.setEnabled(false);
    }//GEN-LAST:event_enviarActionPerformed

